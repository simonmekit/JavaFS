package com.perscholas.java_basics;

public class LogicalOperators {
            public static void main(String[] args)
        {
            boolean x = true;
            boolean y = false;
            System.out.println("x & y : " + (x & y));
            System.out.println("x && y : " + (x && y));
            System.out.println("x | y : " + (x | y));
            System.out.println("x || y: " + (x || y));
            System.out.println("x ^ y : " + (x ^ y));
            System.out.println("!x : " + (!x));
        }
    }
